def check():
    """Stop early with a friendly message if a required value is missing."""
    missing = []
    if not TELEGRAM_BOT_TOKEN or "PASTE" in TELEGRAM_BOT_TOKEN:
        missing.append("TELEGRAM_BOT_TOKEN")
    if not ALLOWED_TELEGRAM_USER_ID or "PASTE" in ALLOWED_TELEGRAM_USER_ID:
        missing.append("ALLOWED_TELEGRAM_USER_ID")
    if not ANTHROPIC_API_KEY or "PASTE" in ANTHROPIC_API_KEY:
        missing.append("ANTHROPIC_API_KEY")
    if not GOOGLE_SHEET_ID or "PASTE" in GOOGLE_SHEET_ID:
        missing.append("GOOGLE_SHEET_ID")
    if not os.path.exists(GOOGLE_SERVICE_ACCOUNT_FILE) and not GOOGLE_CREDENTIALS_JSON:
        missing.append("Google credentials (the JSON file in credentials/, or "
                       "GOOGLE_CREDENTIALS_JSON if you're hosting)")

    if missing:
        print("\n  Setup not finished yet. These are still missing from your .env:")
        for m in missing:
            print(f"   - {m}")
        print("\n  Open your .env file, fill those in, then run the bot again.\n")
        sys.exit(1)
