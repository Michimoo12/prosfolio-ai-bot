"""
config.py
Loads your settings from the .env file and checks nothing important is missing.
You do not edit this file — it just reads your .env.
"""

import os
import sys
import datetime
import pytz
from dotenv import load_dotenv

load_dotenv()

TELEGRAM_BOT_TOKEN = os.getenv("TELEGRAM_BOT_TOKEN", "").strip()
ALLOWED_TELEGRAM_USER_ID = os.getenv("ALLOWED_TELEGRAM_USER_ID", "").strip()
ANTHROPIC_API_KEY = os.getenv("ANTHROPIC_API_KEY", "").strip()
GOOGLE_SERVICE_ACCOUNT_FILE = os.getenv(
    "GOOGLE_SERVICE_ACCOUNT_FILE", "credentials/google-service-account.json"
).strip()
GOOGLE_SHEET_ID = os.getenv("GOOGLE_SHEET_ID", "").strip()
# For cloud hosting only: paste the whole service-account JSON into this one env
# var so you never upload the key file anywhere public. Local runs use the file.
GOOGLE_CREDENTIALS_JSON = os.getenv("GOOGLE_CREDENTIALS_JSON", "").strip()
DEFAULT_USD_TO_PHP_RATE = float(os.getenv("DEFAULT_USD_TO_PHP_RATE", "58.50"))
TIMEZONE = os.getenv("TIMEZONE", "Asia/Manila").strip()

# The model used to read your messages. Haiku is the cheapest and is great at this.
AI_MODEL = "claude-haiku-4-5-20251001"

# Accounts whose "Account Type" (in the Accounts tab) is one of these are
# treated as NOT spendable: /balance shows them under a separate locked
# section and the AI won't count them as money you can spend. Edit the
# Account Type cell in your sheet to e.g. "savings", "investment" or
# "crypto" to lock an account.
ILLIQUID_ACCOUNT_TYPES = {"savings", "investment", "crypto", "locked", "illiquid"}


def now():
    """
    Current time IN YOUR TIMEZONE (Asia/Manila by default).

    Everything that stamps a date/time must use this instead of
    datetime.now()/date.today(). Railway servers run on UTC, which is 8 hours
    behind Manila — with the naive calls, anything you logged between midnight
    and 8am was recorded under YESTERDAY's date (and /report put the start of
    each month into the previous month).
    """
    return datetime.datetime.now(pytz.timezone(TIMEZONE))


def today_iso():
    """Today's date (YYYY-MM-DD) in your timezone."""
    return now().date().isoformat()


# Names of the tabs in your Google Sheet (must match exactly).
SHEET_TRANSACTIONS = "Transactions"
